//算数运算符
#include <iostream>
#include <cstdlib>

using namespace std;

int main()
{
    int A = 21, B = 6;
    //算数运算符的各种运算与结果
    cout << "A = 21, B = 6, A + B = " << A + B << endl;
    cout << "A = 21, B = 6, A - B = " << A - B << endl;
    cout << "A = 21, B = 6, A * B = " << A * B << endl;
    cout << "A = 21, B = 6, A / B = " << A / B << endl;
    cout << "A = 21, B = 6, A % B = " << A % B << endl;         //余数运算符的使用

    system("pause");
    return 0;
}